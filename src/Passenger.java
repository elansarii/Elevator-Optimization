class Passenger {
    private int startFloor;
    private int goalFloor;

    public Passenger(int startFloor, int goalFloor) {
        this.startFloor = startFloor;
        this.goalFloor = goalFloor;
    }

    public int getStartFloor() {
        return startFloor;
    }

    public int getGoalFloor() {
        return goalFloor;
    }
}
